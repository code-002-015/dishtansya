    {

        $product = Product::find($request->product_id);

        if($product->available_stock < 1){

            return $this->sendError('No available stock.', 'Failed to order this product due to unavailability of the stock', 400);

        } else {
            $remaining_qty = $product->available_stock-$request->quantity;
            $product->update([
                'available_stock' => $remaining_qty
            ]);


            $data['available_stock'] = $remaining_qty;


            return $this->sendResponse($data, 'You have successfully ordered this product.');
        }
        
